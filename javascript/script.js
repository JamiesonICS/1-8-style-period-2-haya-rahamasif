

function showList() {
    document.getElementById("awards").style.display = "block";
    document.getElementById("see-awards").style.display = "none";
    document.getElementById("hide-awards").style.display = "block";
  }

  function hideList() {
    document.getElementById("awards").style.display = "none";
    document.getElementById("see-awards").style.display = "block";
    document.getElementById("hide-awards").style.display = "none";
  }